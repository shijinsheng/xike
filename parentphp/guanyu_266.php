		<span style="FONT-SIZE: 20px; COLOR: #c72a35">业界贡献</span><br />
		<span style="FONT-SIZE: 14px; COLOR: #c72a35">成都西科经过多年潜心研发，掌握了多项核心技术，推动了行业的发展。</span><br />
		<span style="FONT-SIZE: 16px">视频图像处理技术</span><br />
		<span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		 图像信号处理(ISP)技术， 数字图像信号处理，包括自动曝光、自动白平衡、自动聚焦、色彩校正、伽玛校正等，使图像更清晰、自然、真实。<br />
		 <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		 图像前处理技术， 对原始视频数据进行去噪、增强、解交织、抗抖动等图像处理，提高图像质量，降低压缩失真。<br />
		 <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		  图像后处理技术，对解码后的视频进行去块效应、锐化、增强等图像处理，使视频显示效果更清晰、流畅、饱满、逼真，提高主观视觉效果。<br />
		  <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		  图像后处理技术，对解码后的视频进行去块效应、锐化、增强等图像处理，使视频显示效果更清晰、流畅、饱满、逼真，提高主观视觉效果。<br /><br />
		  
		  <span style="FONT-SIZE: 16px">视音频编解码技术</span><br />
		  <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		   领先的视音频压缩技术：先进的H.264技术，节省硬盘存储空间，实现高效网络传输；全面具备MPEG4/MPEG2/MJPEG/AVS和G7xx系列、MPEG Audio系列等视音频编解码压缩技术，满足个性化需求。<br />
		   <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		   支持动态调整编码参数，根据不同应用需求，可对分辨率、码率、帧率、图像质量等视频参数进行 动态调整，提高网络适应能力。<br />
		   <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		    支持数字水印、视频加密技术，可在码流中嵌入设备序列号/录制时间等关键特征，防止伪造篡改录像数据，提高视频信息的可信度及安全性。<br /><br />
			
			<span style="FONT-SIZE: 16px">视频分析与模式识别技术</span><br />
			<span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		     视频分析技术，利用背景建模、目标检测、目标跟踪、目标分类等方法从视频中获取运动目标的各种特征，并对视频中发生的各种异常事件进行自动检测及报警，以提升监控系统的主动防御能力及效率。<br />
			 <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		     模式识别技术，利用特征提取及匹配技术从视频图像中提取特定对象的表征数据，并对表征数据进行辨认及描述，实现对海量监控视频的数据挖掘及利用，例如车牌识别及人脸识别技术。<br /><br />
			 
			 <span style="FONT-SIZE: 16px">视音频数据存储技术</span><br />
			 <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		      文件预分配技术，避免删除录像文件时产生磁盘碎片，提高硬盘读写效率，保证系统长时间稳定运行。<br />
			  <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		       文件系统采用Fat32技术，与windows文件系统兼容。<br />
			   <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		        硬盘关键信息区的冻结保护技术，保证文件系统的完整性。<br />
			<span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		     硬盘故障预警和报警技术（S.M.A.R.T.），定时检测硬盘工作状态，对出现问题的硬盘会实时显示错误告警，有效保护用户数据。<br />
			 <span style="FONT-SIZE: 16px; COLOR: #c72a35">-</span>
		      多硬盘延时上电技术和非工作硬盘休眠等先进硬盘控制技术，有效延长硬盘使用寿命，低碳节能。<br /><br />
			  
